function teacherDashboard() {
  const lessons = teacherLessons();
  const teacherStudents = teacherStudentCount();
  const completed = lessons.filter((lesson) => lesson.status === "completed").length;
  const waiting = lessons.filter((lesson) => lesson.status === "waiting").length;
  return `
    <div class="grid">
      <section class="grid stats-grid">
        ${stat("□", lessons.length, tr("myLessons"), tr("today"), "brand")}
        ${stat("○", teacherStudents, tr("totalStudents"), "active", "green")}
        ${stat("✓", completed, tr("completedLessons"), tr("completed"), "blue")}
        ${stat("!", waiting, tr("waitingLessons"), tr("waiting"), "amber")}
      </section>
      <section class="grid teacher-hero">${lessons.map((lesson, index) => {
        const [start, end] = String(lesson.time || "").split(" - ");
        return `
        <div class="lesson-card">
          <div class="lesson-time"><strong>${html(start)}</strong><span>${html(end ? `${end} gacha` : "")}</span></div>
          <div class="subject-icon" style="background:${index % 2 ? "var(--green-soft)" : "var(--brand-soft)"};color:${index % 2 ? "var(--green)" : "var(--brand)"}">${html(String(lesson.subject || "").slice(0, 2))}</div>
          <div><div class="lesson-name">${html(lesson.groupName)}</div><div class="lesson-meta">${html(lesson.subject)} · ${html(lesson.room)}</div></div>
          <button class="soft-button" onclick="openAttendance('${jsString(lesson.id)}')">${html(tr("takeAttendance"))} -></button>
        </div>`;
      }).join("") || `<div class="empty">${html(tr("waitingLessons"))}</div>`}</section>
      <section class="grid wide-grid">
        <div class="card"><div class="card-header"><h2 class="card-title">${html(tr("schedule"))}</h2></div>${lessonsTable(lessons)}</div>
        <div class="card"><div class="card-header"><h2 class="card-title">${html(tr("attendanceHistory"))}</h2></div>${teacherAttendanceList(5)}</div>
      </section>
    </div>`;
}

function stat(icon, value, label, delta, tone) {
  const bg = tone === "brand" ? "var(--brand-soft)" : `var(--${tone}-soft)`;
  const color = tone === "brand" ? "var(--brand)" : `var(--${tone})`;
  return `<div class="stat-card"><div class="stat-icon" style="background:${bg};color:${color}">${html(icon)}</div><div><div class="stat-value">${html(value)}</div><div class="stat-label">${html(label)}</div><div class="stat-delta">${html(delta)}</div></div></div>`;
}

function attendanceWidget(s) {
  const total = s.present + s.absent;
  const percent = total ? Math.round((s.present / total) * 100) : 0;
  return `
    <div class="card-header"><h2 class="card-title">${html(tr("todayAttendance"))}</h2></div>
    <div class="donut-wrap">
      <div class="donut" style="--present:${percent}"><div class="donut-center"><div class="donut-number">${html(total)}</div><div class="donut-label">Jami</div></div></div>
      <div class="legend">
        <div class="legend-row"><span><i class="dot" style="background:var(--green)"></i>${html(tr("present"))}</span><strong>${html(s.present)}</strong></div>
        <div class="legend-row"><span><i class="dot" style="background:var(--red)"></i>${html(tr("absent"))}</span><strong>${html(s.absent)}</strong></div>
      </div>
    </div>`;
}

