  return `<div class="list-row"><div class="mini-avatar">↗</div><div><div class="row-title">${html(message.to)}</div><div class="row-meta">${html(message.text)}</div></div><span class="status ${statusClass(message.status)}">${html(tr(message.status))}</span></div>`;
