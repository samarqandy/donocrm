function val(id) {
  return document.getElementById(id)?.value || "";
}

async function saveModal() {
  const type = state.modal.type;
  const payloads = {
    student: ["/api/students", { name: val("name"), groupId: val("groupId"), parentName: val("parentName"), phone: val("phone"), debt: Number(val("debt")) }],
    group: ["/api/groups", { name: val("name"), subject: val("subject"), teacherId: val("teacherId"), room: val("room"), monthlyFee: Number(val("monthlyFee")) }],
    lesson: ["/api/lessons", { groupId: val("groupId"), time: val("time") }],
    payment: ["/api/payments", { studentId: val("studentId"), amount: Number(val("amount")), type: val("type") }],
    message: ["/api/messages", { to: val("to"), text: val("text") }],
    lead: ["/api/leads", { name: val("name"), phone: val("phone"), source: val("source"), status: val("status"), note: val("note") }],
  };
  try {
    const [path, body] = payloads[type];
    await api(path, { method: "POST", body: JSON.stringify(body) });
    state.modal = null;
    showToast(tr("saved"));
    await load();
  } catch (error) {
    showToast(error.message);
  }
}

async function saveAttendance() {
  const lesson = state.data.lessons.find((l) => l.id === state.modal.lessonId);
  const students = state.data.students.filter((s) => s.groupId === lesson.groupId);
  const records = students.map((student) => ({ studentId: student.id, status: state.attendanceDraft[student.id] || "present" }));
  try {
    await api("/api/attendance", { method: "POST", body: JSON.stringify({ lessonId: lesson.id, records }) });
    state.modal = null;
    showToast(tr("saved"));
    await load();
  } catch (error) {
    showToast(error.message);
  }
}

