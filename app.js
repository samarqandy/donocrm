const app = document.getElementById("app");
const state = {
  lang: "uz",
  role: null,
  userId: null,
  user: null,
  page: "dashboard",
  data: null,
  modal: null,
  attendanceDraft: {},
  search: "",
  mobileNav: false,
  toast: "",
  loading: true,
  authChecked: false,
};

function tr(key) {
  return i18n[state.lang][key] || key;
}

function money(value) {
  return `${Number(value || 0).toLocaleString("ru-RU")} so'm`;
}

function html(value) {
  return String(value ?? "").replace(/[&<>"']/g, (char) => ({
    "&": "&amp;",
    "<": "&lt;",
    ">": "&gt;",
    '"': "&quot;",
    "'": "&#039;",
  })[char]);
}

function attr(value) {
  return html(value);
}

function jsString(value) {
  return String(value ?? "")
    .replace(/\\/g, "\\\\")
    .replace(/'/g, "\\'")
    .replace(/\r/g, "\\r")
    .replace(/\n/g, "\\n")
    .replace(/\u2028/g, "\\u2028")
    .replace(/\u2029/g, "\\u2029");
}

function emptyTableRow(colspan, message = "Ma'lumot yo'q") {
  return `<tr><td colspan="${colspan}"><div class="empty table-empty">${html(message)}</div></td></tr>`;
}

function headers() {
  return {
    "Content-Type": "application/json",
  };
}

async function api(path, options = {}) {
  const res = await fetch(path, { ...options, headers: { ...headers(), ...(options.headers || {}) } });
  const json = await res.json();
  if (!res.ok) throw new Error(json.error || "API error");
  return json;
}

async function load() {
  state.loading = true;
  render();
  try {
    state.data = await api("/api/bootstrap");
  } catch (error) {
    state.toast = error.message;
  } finally {
    state.loading = false;
    render();
  }
}

async function checkAuth() {
  state.loading = true;
  render();
  try {
    const result = await api("/api/me");
    setUser(result.user);
    await load();
  } catch (_error) {
    state.user = null;
    state.role = null;
    state.userId = null;
    state.loading = false;
    state.authChecked = true;
    render();
  }
}

function setUser(user) {
  state.user = user;
  state.role = user.role;
  state.userId = user.id;
  state.authChecked = true;
}

function setState(patch) {
  Object.assign(state, patch);
  render();
}

function showToast(message) {
  state.toast = message;
  render();
  clearTimeout(showToast.timer);
  showToast.timer = setTimeout(() => {
    state.toast = "";
    render();
  }, 2800);
}

function statusClass(status) {
  if (["completed", "sent", "converted"].includes(status)) return "success";
  if (["waiting", "queued", "contacted", "new"].includes(status)) return "warning";
  if (status === "failed") return "danger";
  return "info";
}

function initials(name = "") {
  return name.split(" ").map((part) => part[0]).join("").slice(0, 2).toUpperCase();
}

